#include "libft.h"

char	*ft_strchr( const char *str, int c)
{
	int		i;
	char	*s1;

	i = 0;
	s1 = (char *)str;
	while (s1[i] != (char)c)
	{
		if (s1[i] == '\0')
			return (NULL);
		i++;
	}
	return (&s1[i]);
}
