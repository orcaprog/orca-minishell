#include "../Include/minishell.h"

int	ch_join(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '+' && str[i] != '=' && str[i])
	{
		i++;
		if (str[i] == '+' && str[i + 1] == '=')
			return (1);
	}
	return (0);
}
